public class Equations {
    public static void main(String[] args) {
        double x1 = 1.2;
        double y1 = 14;
        double result1 = Math.pow(3 * x1 * x1 + 7 / y1 / y1, 2);
        System.out.println("Sonuç 1: " + result1);

        double x2 = 6;
        double y2 = 4;
        double result2 = (x2 * x2 + 2 * y2) / ((y2 * y2) / 2);
        System.out.println("Sonuç 2: " + result2);
    }
}
